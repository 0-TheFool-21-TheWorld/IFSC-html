n = parseInt(prompt("Digite a o número do mês "));

switch (n) {
    case 1: {
        document.write("<p>Janeiro</p>");
        break;
    }
    case 2: {
        document.write("<p>Fevereiro</p>");
        break;
    }
    case 3: {
        document.write("<p>Março</p>");
        break;
    }
    case 4: {
        document.write("<p>Abril</p>");
        break;
    }
    case 5: {
        document.write("<p>Maio</p>");
        break;
    }
    case 6: {
        document.write("<p>Junho</p>");
        break;
    }
    case 7: {
        document.write("<p>Julho</p>");
        break;
    }
    case 8: {
        document.write("<p>Agosto</p>");
        break;
    }
    case 9: {
        document.write("<p>Setembro</p>");
        break;
    }
    case 10: {
        document.write("<p>Outubro</p>");
        break;
    }
    case 11: {
        document.write("<p>Novembro</p>");
        break;
    }
    case 12: {
        document.write("<p>Dezembro</p>");
        break;
    }

}